-- Keep the legacy management RPC compatible while sourcing actuals only from
-- currently qualified order snapshots. Revoked orders disappear immediately;
-- restored orders are counted again by their retained attribution quarter.
create or replace function public.get_performance_management_dashboard(p_quarter_start date)
returns table(
  profile_id uuid,
  profile_name text,
  quarter_start date,
  points_target numeric,
  estimated_points numeric,
  official_points numeric,
  new_gmv_target numeric,
  new_gmv_actual numeric,
  renewal_gmv_target numeric,
  renewal_gmv_actual numeric,
  monthly_observations jsonb,
  can_set_target boolean
)
language plpgsql security definer stable set search_path=''
as $get_performance_management_dashboard$
declare
  v_profile public.profiles;
begin
  if p_quarter_start is null
    or p_quarter_start<>date_trunc('quarter',p_quarter_start)::date then
    raise exception 'INVALID_QUARTER' using errcode='22023';
  end if;
  select p.* into v_profile
  from public.profiles p
  where p.id=auth.uid() and p.status='active';
  if v_profile.id is null
    or not public.is_feature_enabled(v_profile.team_id,'sales_os_v3') then
    raise exception 'PERFORMANCE_FORBIDDEN' using errcode='42501';
  end if;

  return query
  with people as materialized(
    select p.id,p.name
    from public.profiles p
    where p.team_id=v_profile.team_id
      and p.status='active'
      and exists(
        select 1
        from public.profile_access_roles par
        join public.access_roles ar
          on ar.id=par.role_id and ar.team_id=par.team_id
        where par.team_id=p.team_id and par.profile_id=p.id and ar.code='sales'
      )
      and(
        p.id=v_profile.id
        or public.can_supervise_performance(
          v_profile.team_id,p.id,(now() at time zone 'Asia/Shanghai')::date
        )
        or public.has_access_role(v_profile.team_id,array['owner','admin'])
        or public.has_permission(v_profile.team_id,'finance.read')
      )
  ), actuals as materialized(
    select
      s.salesperson_id,
      coalesce(sum(s.points_snapshot),0)::numeric points,
      coalesce(sum(s.gmv_snapshot) filter(where s.sale_type='new'),0)::numeric new_gmv,
      coalesce(sum(s.gmv_snapshot) filter(where s.sale_type='renewal'),0)::numeric renewal_gmv
    from public.order_performance_states s
    join people p on p.id=s.salesperson_id
    where s.team_id=v_profile.team_id
      and s.attribution_quarter_start=p_quarter_start
      and s.status='qualified'
    group by s.salesperson_id
  ), months as(
    select generate_series(
      p_quarter_start,p_quarter_start+interval '2 months',interval '1 month'
    )::date month_start
  )
  select
    p.id,
    p.name,
    p_quarter_start,
    coalesce(t.points_target,0),
    coalesce((
      select sum(o.estimated_points)
      from public.performance_monthly_observations o
      where o.team_id=v_profile.team_id
        and o.profile_id=p.id
        and o.month_start between p_quarter_start and p_quarter_start+interval '2 months'
    ),t.estimated_points,0),
    coalesce(a.points,0),
    coalesce(t.new_gmv_target,0),
    coalesce(a.new_gmv,0),
    coalesce(t.renewal_gmv_target,0),
    coalesce(a.renewal_gmv,0),
    (
      select jsonb_agg(jsonb_build_object(
        'month_start',m.month_start,
        'estimated_points',coalesce(o.estimated_points,0),
        'new_gmv',coalesce(x.new_gmv,0),
        'renewal_gmv',coalesce(x.renewal_gmv,0),
        'official_points',coalesce(x.points,0)
      ) order by m.month_start)
      from months m
      left join public.performance_monthly_observations o
        on o.team_id=v_profile.team_id
        and o.profile_id=p.id
        and o.month_start=m.month_start
      left join lateral(
        select
          coalesce(sum(s.points_snapshot),0)::numeric points,
          coalesce(sum(s.gmv_snapshot) filter(where s.sale_type='new'),0)::numeric new_gmv,
          coalesce(sum(s.gmv_snapshot) filter(where s.sale_type='renewal'),0)::numeric renewal_gmv
        from public.order_performance_states s
        where s.team_id=v_profile.team_id
          and s.salesperson_id=p.id
          and s.status='qualified'
          and s.attribution_quarter_start=p_quarter_start
          and date_trunc('month',coalesce(s.last_qualified_at,s.first_qualified_at))::date=m.month_start
      ) x on true
    ),
    (
      public.can_supervise_performance(
        v_profile.team_id,p.id,(now() at time zone 'Asia/Shanghai')::date
      )
      or public.has_access_role(v_profile.team_id,array['owner','admin'])
    )
  from people p
  left join public.performance_quarterly_targets t
    on t.team_id=v_profile.team_id
    and t.profile_id=p.id
    and t.quarter_start=p_quarter_start
  left join actuals a on a.salesperson_id=p.id
  order by p.name,p.id;
end
$get_performance_management_dashboard$;

