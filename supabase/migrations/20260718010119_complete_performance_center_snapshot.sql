create index if not exists order_performance_states_quarter_sales_qualified_idx
  on public.order_performance_states(team_id,attribution_quarter_start,salesperson_id)
  where status='qualified';

create index if not exists order_performance_events_latest_order_idx
  on public.order_performance_events(team_id,order_id,created_at desc,id desc);

-- Payment, reversal and cancellation rows are the trusted ledgers. Keep the
-- transition engine out of the exposed public schema and invoke it only from
-- database triggers, so a browser cannot manufacture qualification events.
create schema if not exists sales_os_private;
revoke all on schema sales_os_private from public,anon,authenticated,service_role;

create or replace function sales_os_private.refresh_order_performance_state_core(
  p_team_id text,
  p_order_id uuid,
  p_reason text,
  p_trigger_type text,
  p_trigger_id uuid,
  p_actor_id uuid
) returns void
language plpgsql security definer set search_path=''
as $refresh_order_performance_state_core$
declare
  v_order public.deal_orders;
  v_state public.order_performance_states;
  v_prior_event public.order_performance_events;
  v_event_type text;
  v_net_paid numeric;
  v_quarter_start date;
  v_is_cancelled boolean:=false;
  v_idempotency_key uuid;
begin
  if p_team_id is null
    or p_order_id is null
    or nullif(trim(p_reason),'') is null
    or p_trigger_type not in('payment','reversal','order','backfill')
    or p_trigger_id is null
    or (p_actor_id is null and p_trigger_type<>'backfill') then
    raise exception 'PERFORMANCE_TRIGGER_INPUT_REQUIRED' using errcode='22023';
  end if;

  select o.* into v_order
  from public.deal_orders o
  where o.team_id=p_team_id and o.id=p_order_id
  for update;
  if v_order.id is null then
    raise exception 'PERFORMANCE_TRIGGER_ORDER_NOT_FOUND' using errcode='P0002';
  end if;
  if p_actor_id is not null and not exists(
      select 1
      from public.profiles p
      where p.id=p_actor_id and p.team_id=p_team_id
    ) then
    raise exception 'PERFORMANCE_TRIGGER_ACTOR_MISMATCH' using errcode='42501';
  end if;
  if not public.is_feature_enabled(p_team_id,'sales_os_v3') then
    return;
  end if;

  -- Namespace the ledger row id by trigger type. This key is generated by the
  -- database and cannot be supplied by a client.
  v_idempotency_key:=md5(
    'order-performance:'||p_trigger_type||':'||p_trigger_id::text
  )::uuid;
  select e.* into v_prior_event
  from public.order_performance_events e
  where e.team_id=p_team_id and e.idempotency_key=v_idempotency_key;
  if v_prior_event.id is not null then
    if v_prior_event.order_id<>p_order_id
      or v_prior_event.trigger_type<>p_trigger_type
      or v_prior_event.trigger_id is distinct from p_trigger_id then
      raise exception 'PERFORMANCE_TRIGGER_IDEMPOTENCY_CONFLICT' using errcode='23505';
    end if;
    return;
  end if;

  select greatest(
    coalesce(sum(p.amount),0)-coalesce((
      select sum(r.amount)
      from public.deal_payment_reversals r
      join public.deal_payments rp
        on rp.id=r.payment_id and rp.team_id=r.team_id
      where rp.team_id=p_team_id and rp.order_id=p_order_id
    ),0),
    0
  ) into v_net_paid
  from public.deal_payments p
  where p.team_id=p_team_id and p.order_id=p_order_id;

  -- Cancellation is an append-only business record. It revokes performance
  -- even though the legacy cancellation RPC does not mutate deal_orders.status.
  select
    v_order.status='cancelled'
    or exists(
      select 1
      from public.deal_order_cancellations c
      where c.team_id=p_team_id and c.order_id=p_order_id
    )
  into v_is_cancelled;

  select s.* into v_state
  from public.order_performance_states s
  where s.team_id=p_team_id and s.order_id=p_order_id
  for update;
  if v_state.order_id is null then
    insert into public.order_performance_states(
      team_id,order_id,salesperson_id,sale_type,gmv_snapshot,
      points_snapshot,net_customer_paid
    ) values(
      v_order.team_id,v_order.id,v_order.salesperson_id_snapshot,
      v_order.sale_type_snapshot,v_order.performance_gmv_snapshot,
      v_order.performance_points_snapshot,v_net_paid
    )
    returning * into v_state;
  end if;

  if v_net_paid>=v_order.customer_total
    and not v_is_cancelled
    and v_state.status<>'qualified' then
    v_event_type:=case
      when v_state.first_qualified_at is null then 'qualified'
      else 'restored'
    end;
    v_quarter_start:=coalesce(
      v_state.attribution_quarter_start,
      date_trunc('quarter',(now() at time zone 'Asia/Shanghai'))::date
    );
    update public.order_performance_states s set
      status='qualified',
      attribution_quarter_start=v_quarter_start,
      net_customer_paid=v_net_paid,
      first_qualified_at=coalesce(s.first_qualified_at,now()),
      last_qualified_at=now(),
      revoked_at=null,
      updated_at=now()
    where s.team_id=p_team_id and s.order_id=p_order_id
    returning * into v_state;
  elsif (v_net_paid<v_order.customer_total or v_is_cancelled)
    and v_state.status='qualified' then
    v_event_type:='revoked';
    v_quarter_start:=v_state.attribution_quarter_start;
    update public.order_performance_states s set
      status='revoked',
      net_customer_paid=v_net_paid,
      revoked_at=now(),
      updated_at=now()
    where s.team_id=p_team_id and s.order_id=p_order_id
    returning * into v_state;
  else
    update public.order_performance_states s set
      net_customer_paid=v_net_paid,
      updated_at=now()
    where s.team_id=p_team_id and s.order_id=p_order_id;
    return;
  end if;

  insert into public.order_performance_events(
    team_id,order_id,event_type,attribution_quarter_start,reason,
    trigger_type,trigger_id,idempotency_key,salesperson_id,sale_type,
    gmv_snapshot,points_snapshot,net_customer_paid,actor_id
  ) values(
    v_order.team_id,v_order.id,v_event_type,v_quarter_start,trim(p_reason),
    p_trigger_type,p_trigger_id,v_idempotency_key,
    v_order.salesperson_id_snapshot,v_order.sale_type_snapshot,
    v_order.performance_gmv_snapshot,v_order.performance_points_snapshot,
    v_net_paid,p_actor_id
  );

  -- Live ledger transitions have a verified business actor and are mirrored to
  -- the global audit log. Migration backfill has no human actor; its immutable
  -- order_performance_events row is the audit trail, avoiding a forged actor.
  if p_actor_id is not null then
    insert into public.audit_logs(
      team_id,actor_id,action,target_type,target_id,before_data,after_data
    ) values(
      v_order.team_id,p_actor_id,'performance.order_'||v_event_type,
      'deal_order',v_order.id,'{}'::jsonb,
      jsonb_build_object(
        'status',v_state.status,
        'quarterStart',v_state.attribution_quarter_start,
        'gmv',v_state.gmv_snapshot,
        'points',v_state.points_snapshot,
        'netCustomerPaid',v_net_paid,
        'triggerType',p_trigger_type,
        'triggerId',p_trigger_id
      )
    );
  end if;
end
$refresh_order_performance_state_core$;

create or replace function sales_os_private.refresh_performance_after_payment()
returns trigger
language plpgsql security definer set search_path=''
as $refresh_performance_after_payment$
begin
  perform sales_os_private.refresh_order_performance_state_core(
    new.team_id,new.order_id,'customer_payment_confirmed','payment',new.id,new.confirmed_by
  );
  return new;
end
$refresh_performance_after_payment$;

create or replace function sales_os_private.refresh_performance_after_reversal()
returns trigger
language plpgsql security definer set search_path=''
as $refresh_performance_after_reversal$
declare
  v_order_id uuid;
begin
  select p.order_id into v_order_id
  from public.deal_payments p
  where p.team_id=new.team_id and p.id=new.payment_id;
  if v_order_id is null then
    raise exception 'PERFORMANCE_TRIGGER_PAYMENT_NOT_FOUND' using errcode='P0002';
  end if;
  perform sales_os_private.refresh_order_performance_state_core(
    new.team_id,v_order_id,'customer_payment_reversed','reversal',new.id,new.confirmed_by
  );
  return new;
end
$refresh_performance_after_reversal$;

create or replace function sales_os_private.refresh_performance_after_cancellation()
